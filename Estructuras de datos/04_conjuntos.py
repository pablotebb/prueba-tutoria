
# Crear un conjunto con elementos duplicados
conjunto = {1, 2, 3, 4, 5, 5, 6, 7, 7, 8}

# Convertir el conjunto en una lista para acceder al tercer elemento
lista_conjunto = list(conjunto)

# Acceder al tercer elemento de la lista (índice 2)
tercer_elemento = lista_conjunto[2]

# Mostrar el tercer elemento
print("Tercer elemento del conjunto:", tercer_elemento)
# Tercer elemento del conjunto: 3


# Para acceder al tercer elemento de un conjunto en Python, primero necesitarías convertir el conjunto en una lista (ya que los conjuntos no mantienen un orden predecible), y luego acceder al elemento en la posición 2 (índice 2).
